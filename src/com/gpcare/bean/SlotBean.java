package com.gpcare.bean;

public class SlotBean {

	public int id;
	public Boolean status;
	
	public SlotBean(int id, Boolean status) {
		super();
		this.id = id;
		this.status = status;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public Boolean getStatus() {
		return status;
	}

	public void setStatus(Boolean status) {
		this.status = status;
	}
}
